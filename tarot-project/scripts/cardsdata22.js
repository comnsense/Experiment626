/**
 * Филтрира картите по различни критерии
 */
const cardFilters = {
    /**
     * Филтър по тип (Големи/Малки аркани)
     */
    byArcana: (type) => {
        if (type === 'all') return tarotCards;
        if (type === 'major') return tarotCards.filter(c => c.suit === 'major');
        if (type === 'minor') return tarotCards.filter(c => c.suit !== 'major');
        return tarotCards;
    },

    /**
     * Филтър по боя (за Малки аркани)
     */
    bySuit: (suit) => {
        if (suit === 'all') return tarotCards;
        return tarotCards.filter(c => c.suit === suit);
    },

    /**
     * Филтър за придворни карти
     */
    byCourtCard: () => {
        return tarotCards.filter(c => 
            c.cardName.includes('Паж') || 
            c.cardName.includes('Рицар') || 
            c.cardName.includes('Царица') || 
            c.cardName.includes('Цар') ||
            c.cardName.includes('Knight') ||
            c.cardName.includes('Queen') ||
            c.cardName.includes('King') ||
            c.cardName.includes('Page')
        );
    },

    /**
     * Филтър за числови карти (Асо до 10)
     */
    byNumberCard: () => {
        return tarotCards.filter(c => 
            !c.cardName.includes('Паж') && 
            !c.cardName.includes('Рицар') && 
            !c.cardName.includes('Царица') && 
            !c.cardName.includes('Цар') &&
            c.suit !== 'major'
        );
    },

    /**
     * Филтър по ключови думи
     */
    byKeyword: (keyword) => {
        const term = keyword.toLowerCase();
        return tarotCards.filter(c => 
            c.keywords.some(k => k.toLowerCase().includes(term))
        );
    },

    /**
     * Филтър по планета
     */
    byPlanet: (planet) => {
        const term = planet.toLowerCase();
        return tarotCards.filter(c => 
            c.planet && c.planet.toLowerCase().includes(term)
        );
    },

    /**
     * Филтър по зодиакален знак
     */
    byZodiac: (zodiac) => {
        const term = zodiac.toLowerCase();
        return tarotCards.filter(c => 
            c.zodiac && c.zodiac.toLowerCase().includes(term)
        );
    },

    /**
     * Филтър по номер (число)
     */
    byNumber: (number) => {
        return tarotCards.filter(c => c.number == number);
    },

    /**
     * Комбиниран филтър
     */
    combinedFilter: (filters) => {
        let result = [...tarotCards];
        
        if (filters.arcana && filters.arcana !== 'all') {
            result = result.filter(c => 
                filters.arcana === 'major' ? c.suit === 'major' : c.suit !== 'major'
            );
        }
        
        if (filters.suit && filters.suit !== 'all') {
            result = result.filter(c => c.suit === filters.suit);
        }
        
        if (filters.courtOnly) {
            result = result.filter(c => 
                c.cardName.includes('Паж') || 
                c.cardName.includes('Рицар') || 
                c.cardName.includes('Царица') || 
                c.cardName.includes('Цар')
            );
        }
        
        if (filters.numberCardsOnly) {
            result = result.filter(c => 
                !c.cardName.includes('Паж') && 
                !c.cardName.includes('Рицар') && 
                !c.cardName.includes('Царица') && 
                !c.cardName.includes('Цар') &&
                c.suit !== 'major'
            );
        }
        
        if (filters.keyword) {
            const term = filters.keyword.toLowerCase();
            result = result.filter(c => 
                c.keywords.some(k => k.toLowerCase().includes(term))
            );
        }
        
        if (filters.planet) {
            const term = filters.planet.toLowerCase();
            result = result.filter(c => 
                c.planet && c.planet.toLowerCase().includes(term)
            );
        }
        
        if (filters.zodiac) {
            const term = filters.zodiac.toLowerCase();
            result = result.filter(c => 
                c.zodiac && c.zodiac.toLowerCase().includes(term)
            );
        }
        
        return result;
    },

    /**
     * Търсене по текст (име, ключови думи, значение)
     */
    search: (query) => {
        const term = query.toLowerCase();
        return tarotCards.filter(c => 
            c.cardName.toLowerCase().includes(term) ||
            c.name.toLowerCase().includes(term) ||
            c.keywords.some(k => k.toLowerCase().includes(term)) ||
            c.meaning.toLowerCase().includes(term) ||
            (c.asSituation && c.asSituation.toLowerCase().includes(term))
        );
    },

    /**
     * Взема всички уникални планети
     */
    getAllPlanets: () => {
        const planets = tarotCards
            .map(c => c.planet)
            .filter(p => p && p !== '—' && p !== '')
            .filter((value, index, self) => self.indexOf(value) === index);
        return planets.sort();
    },

    /**
     * Взема всички уникални зодиакални знаци
     */
    getAllZodiacs: () => {
        const zodiacs = tarotCards
            .map(c => c.zodiac)
            .filter(z => z && z !== '—' && z !== '')
            .filter((value, index, self) => self.indexOf(value) === index);
        return zodiacs.sort();
    }
};


