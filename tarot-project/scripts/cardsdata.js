    hasCard(cards, cardId) {
        return cards.some(c => c.id === cardId);
    }

    countSuit(cards, suit) {
        return cards.filter(c => c.suit === suit).length;
    }

    countNumber(cards, number) {
        return cards.filter(c => c.number == number).length;
    }

    hasNumber(cards, number) {
        return cards.some(c => c.number == number);
    }

    interpret(cards, questionType = 'general') {
        const result = {
            elementalTheme: null,
            specialCombinations: [],
            questionAnswer: null
        };

        for (const rule of this.rules.elementalRules) {
            if (rule.condition(cards)) {
                result.elementalTheme = rule.result;
                break;
            }
        }

        const allRules = [
            ...this.rules.loveRules,
            ...this.rules.careerRules,
            ...this.rules.warningRules,
            ...this.rules.blessingRules,
            ...this.rules.elementalCombos,
            ...this.rules.numberCombos,
            ...this.rules.majorArcanaCombos
        ];

        for (const rule of allRules) {
            if (rule.condition && rule.condition(cards)) {
                result.specialCombinations.push(rule.result);
            }
        }

        if (questionType !== 'general' && this.rules.questionRules[questionType]) {
            for (const rule of this.rules.questionRules[questionType]) {
                if (rule.condition(cards)) {
                    result.questionAnswer = rule.answer;
                    break;
                }
            }
        }

        return result;
    }
